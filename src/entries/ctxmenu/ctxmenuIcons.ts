	["black", "white"].map((color) => [
		color,
		Object.fromEntries(
			Object.entries(iconsMapping).map(([iconName, iconFileName]) => [
				iconName,
				{
					16: svgB64Colored(iconsImport[iconFileName], color, true),
				},
			])
		),
	])
